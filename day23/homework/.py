# 1) გადააქციეთ string "goa" დიდ ასოებად .upper()-ის გამოყენებით

print("goa".upper())

# 2) გადააქციეთ string "PROGRAMMING" პატარა ასოებად .lower()-ის გამოყენებით

print("PROGRAMMING".lower())

# 3) გამოიყენეთ ფუნქცია .capitalize() სტრინგზე: "python"

print("python".capitalize())

# 4) .find() ფუნქციის გამოყენებით იპოვეთ ასობგერა "l" სიტყვა "hello"-ში. ახსენით კომენტარებით თუ რატომ ვიღებთ მიღებულ შედეგს

print("hello".find("l"))
